	// switch case with default statement :
public class switchCase {
		public static void main(String args[]) {
			
		int a = 3;
		switch (a) {
		   case 1:
			   System.out.println("Monday");
		   break;
		   case 2:
			   System.out.println("Tuesday");
		   break;
		   case 3:
			   System.out.println("Wednesday");
		   default:
			   System.out.println("Weekday");
		}
			
		}
}		  // switch case with default statement :
