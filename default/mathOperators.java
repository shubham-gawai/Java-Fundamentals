		// how math operators work :
public class mathOperators {
	public static void main(String args[]) {
		int num1=10;
		int num2=20;
		
		int add_result = num1+num2;
		 
		System.out.println(add_result);
		
		int subsraction_result=num1-num2;
		System.out.println(subsraction_result);
		
		int mul_result=num1*num2;
		System.out.println(mul_result);
		
		int division_result=num1 % num2;
		System.out.println(division_result);
	}

}
