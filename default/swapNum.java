		// Program to swap two numbers :

public class swapNum {
	public static void main(String args[]){
		int a=20;
		int b=10;
		
	    System.out.println(a);
	    System.out.println(b);
	    
	    System.out.println("after swapping");
	    
	    int temp=0;
	    a= temp;		// a become 0 here
	    temp=b;			// now b is temp
	    a=b;
	    
	    
	    System.out.println(a);
	    System.out.println(b);
	  
	}
}


